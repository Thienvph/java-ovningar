package se.inceptive;

import org.junit.Test;

import java.util.ArrayList;

import static org.junit.Assert.assertEquals;



// Enhetstest för inlämningen getLongestLineLength

public class getLongestLineLengthTest {
    @Test
    public void testGetLongestLineLengthSingle() {
        ArrayList<String> input = new ArrayList<String>();
        input.add("1");
        int expected = 1;

        int actual = App.getLongestLineLength(input);

        assertEquals(expected, actual);
    }

    @Test
    public void testGetLongestLineLengthMultiple() {
        ArrayList<String> input = new ArrayList<String>();
        input.add("1");
        input.add("123456");
        int expected = 6;

        int actual = App.getLongestLineLength(input);

        assertEquals(expected, actual);
    }

    @Test
    public void testGetLongestLineLengthLots() {
        ArrayList<String> input = new ArrayList<String>();
        input.add("1234567890123456789012345678901234567890");
        input.add("1234567890123456789012345678901234567890");
        input.add("12345678901234567890123456789012345678901234567890123456789012345678901234567890");

        int expected = 80;

        int actual = App.getLongestLineLength(input);

        assertEquals(expected, actual);
    }

    @Test
    public void testGetLongestLineLengthNone() {
        ArrayList<String> input = new ArrayList<String>();
        int expected = 0;

        int actual = App.getLongestLineLength(input);

        assertEquals(expected, actual);
    }
}
